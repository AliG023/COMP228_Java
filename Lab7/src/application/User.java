package application;

public class User {

	private int id;
	private String fullName;
	private String currentAddress;
	private int contactNumber;
	private String emailAddress;
	private String highestEducation;
	private String gender;
	private String dateAvailable;
	private String desiredPosition;
	private int desiredSalary;
	private String areYouLegal;
	private String relativesWorking;
	private String ifYes;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getfullName() {
		return fullName;
	}

	public void setfullName(String fullName) {
		this.fullName = fullName;
	}

	public String getcurrentAddress() {
		return currentAddress;
	}

	public void setcurrentAddress(String currentAddress) {
		this.currentAddress = currentAddress;
	}

	public int getContactNumber() {
		return contactNumber;
	}

	public void setContactNumber(int contactNumber) {
		this.contactNumber = contactNumber;
	}

	public String getEmailAddress() {
		return emailAddress;
	}

	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}

	public String getHighestEducation() {
		return highestEducation;
	}

	public void setHighestEducation(String highestEducation) {
		this.highestEducation = highestEducation;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getDateAvailable() {
		return dateAvailable;
	}

	public void setDateAvailable(String dateAvailable) {
		this.dateAvailable = dateAvailable;
	}

	public String getDesiredPosition() {
		return desiredPosition;
	}

	public void setDesiredPosition(String desiredPosition) {
		this.desiredPosition = desiredPosition;
	}

	public int getDesiredSalary() {
		return desiredSalary;
	}

	public void setDesiredSalary(int desiredSalary) {
		this.desiredSalary = desiredSalary;
	}

	public String getAreYouLegal() {
		return areYouLegal;
	}

	public void setAreYouLegal(String areYouLegal) {
		this.areYouLegal = areYouLegal;
	}

	public String getRelativesWorking() {
		return relativesWorking;
	}

	public void setRelativesWorking(String relativesWorking) {
		this.relativesWorking = relativesWorking;
	}

	public String getIfYes() {
		return ifYes;
	}

	public void setIfYes(String ifYes) {
		this.ifYes = ifYes;
	}
}
